		<h2 class="zag">Металлообработка</h2>
		<div class="flex">
			<div class="obr1 obr" id="obr1">
				<img src="http://metall-mix.ru/wp-content/uploads/2018/11/cink.jpg">
				<p class="oo">Оцинкование</p>
				<div class="obrr1 obrr">
					<p>
						Оцинкование – покрытие металлических изделий цинком (в целях защиты от коррозии) путем
						погружения в расплавленный цинк (горячее оцинкование) или электролизную ванну (холодное
						оцинкование).Существует несколько различных методов оцинковки металлов, которые сегодня
						применяются как в промышленных, так и в лабораторных условиях. Наиболее популярным из них
						является метод горячей оцинковки металла, когда готовое изделие погружают в расплавленный
						цинк. Такой вариант оцинковки идеален для использования в промышленных условиях, и
						позволяет добиться максимально высокой защиты металлических изделий от коррозии.
					</p>
				
				</div>
			</div>
			<div class="obr2 obr">
				<img src="http://metall-mix.ru/wp-content/uploads/2018/11/kovka.jpg">
				<p class="oo">Ковка</p>
				<div class="obrr2 obrr">
					<p>
						Ковка – это технологический процесс обработки различных металлических заготовок,
						направленный на изменение их формы и размеров. Изделия и полуфабрикаты, получаемые в
						результате ковки, называют поковкой.
					</p>
				
				</div>
			</div>
			<div class="obr3 obr">
				<img src="http://metall-mix.ru/wp-content/uploads/2018/11/rezka.jpg">
				<p class="oo">Резка</p>
				<div class="obrr3 obrr">
					<p>
						Резка металла – это технологический процесс, целью которого является разделение листового или
						сортового металла на части и заготовки, либо изготовление деталей заданных форм и размеров.
						Для резки применяются токарные станки, гильотины, фрезеровочные станки, лазерные станки,
						отрезные станки, буровые пресса и другое специальное металлорежущее оборудование,
						использующее различные технологии резки металла и разные режущие инструменты.
					</p>
				
				</div>
			</div>
		</div>
		<div class="flex flex2">
			<div class="obr4 obr">
				<img src="http://metall-mix.ru/wp-content/uploads/2018/11/gibka.jpg">
				<p class="oo">Гибка</p>
				<div class="obrr4 obrr">
					<p>
						Гибка металла - процесс по обработке листовых металлических деталей, в
						результате которого они приобретают необходимую форму. Сегодня эта операция пользуется
						огромным спросом.
						Зачастую гибка металлов выполняется благодаря сжатию внутренних и растягиванию всех
						наружных слоев. Неизменными в размерах остаются лишь те слои, которые находятся вдоль оси
						изгиба.
					</p>
				
				</div>
			</div>
			<div class="obr5 obr">
				<img src="http://metall-mix.ru/wp-content/uploads/2018/11/pesko.jpg">
				<p class="oo">Обработка пескоструйная</p>
				<div class="obrr5 obrr">
					<p>
						Обработка пескоструйная – очистка различных металлических поверхностей от продуктов
						коррозии, нефтепродуктов (биту­ма, мазута и др.), старых лакокрасочных покрытий, штукатурки и
						дру­гих видов загрязнений посредством пескоструйного оборудования , действие которых
						основано на подаче струи сжатого воздуха со взвешенными в нем частицами песка на
						обрабатываемую поверхность.
					</p>
				
				</div>
			</div>
